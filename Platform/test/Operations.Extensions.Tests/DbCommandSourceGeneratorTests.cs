    [Fact]
    public async Task DbCommand_SpOnly_NoValidICommand_GeneratesDbOpsAndError() // Updated name and expectation
    {
        var source = """
        using Operations.Extensions.Dapper;
        namespace TestNamespace;

        // This command does NOT implement ICommand<TResult>
        [DbCommand(sp: "test_sp")]
        public partial record MyCommand { public int Id { get; set; } }
        """;

        var expectedDbOps = GetExpectedToDbParamsSource("MyCommand", new[] { ("Id", "Id") }, isRecord: true);

        var expectedDiagnostic = CSharpSourceGeneratorVerifier<DbCommandSourceGenerator>.Diagnostic("DBCOMMANDGEN002")
            .WithMessageFormat("Class '{0}' is decorated with DbCommandAttribute specifying 'sp' or 'sql' for handler generation, but it does not implement ICommand<TResult>. Handler cannot be generated without a result type.")
            .WithArguments("MyCommand")
            .WithSeverity(DiagnosticSeverity.Error)
            .WithLocation(0); // Or more specific location if possible

        var test = new CSharpSourceGeneratorTest<DbCommandSourceGenerator, XUnitVerifier>
        {
            TestState =
            {
                // ICommandInterfacesConstant is not strictly needed here as MyCommand doesn't use it,
                // but good for consistency if other parts of the test infra might pick it up.
                Sources = { source, ICommandInterfacesConstant },
                GeneratedSources =
                {
                    (typeof(DbCommandSourceGenerator), "TestNamespace.MyCommand.DbOps.g.cs", expectedDbOps),
                },
                ExpectedDiagnostics = { expectedDiagnostic },
                ReferenceAssemblies = Net6ReferenceAssemblies,
                AdditionalReferences = { typeof(DbCommandAttribute).Assembly, typeof(Dapper.DynamicParameters).Assembly, typeof(NpgsqlDataSource).Assembly }
            }
        };

        // Check that only one file (DbOps) is generated due to the error.
        // The test framework might require all listed generated files to be present
        // or have a way to specify "this file should NOT be generated".
        // For now, we ensure only DbOps is listed and an error diagnostic is present.
        // If the framework tries to match more files than listed, it's fine.
        // If it errors because a handler file is missing (and it shouldn't be generated), that's good.
        // To explicitly test non-generation of handler, we'd check test.Result.GeneratedSources.Count
        await test.RunAsync();
        Assert.Equal(1, test.Result.GeneratedSources.Length); // Check only one file generated
    }

