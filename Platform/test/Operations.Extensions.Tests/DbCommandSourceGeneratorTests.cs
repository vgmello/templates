    [Fact]
    public async Task DbCommand_SpOnly_NoICommand_GeneratesDbOpsOnly()
    {
        var source = """
        using Operations.Extensions.Dapper;
        namespace TestNamespace;

        [DbCommand(sp: "test_sp")]
        public partial class MyCommand { public int Id { get; set; } }
        """;

        var expectedDbOps = """
        // <auto-generated/>
        #nullable enable

        namespace TestNamespace;

        public partial class MyCommand : global::Operations.Extensions.Dapper.IDbParamsProvider
        {
            public global::Dapper.DynamicParameters ToDbParams()
            {
                var p = new global::Dapper.DynamicParameters();
                p.Add("Id", this.Id);
                return p;
            }
        }

        """;

        var test = new CSharpSourceGeneratorTest<DbCommandSourceGenerator, XUnitVerifier>
        {
            TestState =
            {
                Sources = { source },
                GeneratedSources =
                {
                    (typeof(DbCommandSourceGenerator), "TestNamespace.MyCommand.DbOps.g.cs", expectedDbOps),
                    // Expect NO handler file
                },
                ReferenceAssemblies = Net6ReferenceAssemblies,
                AdditionalReferences = { typeof(DbCommandAttribute).Assembly, typeof(Dapper.DynamicParameters).Assembly }
            }
        };
        // Verify that only one file is generated
        test.TestState.GeneratedSources.RemoveAt(1); // Remove if a slot for handler was implicitly added by helper
        await test.RunAsync();
         Assert.Single(test.Result.GeneratedSources); // Ensure only DbOps was generated
    }

    [Fact]
    public async Task DbCommand_SqlOnly_ICommand_GeneratesDbOpsAndHandler_NonQuery()
    {
        var source = """
        using Operations.Extensions.Dapper;
        using Operations.Extensions.Messaging;
        namespace TestNamespace;

        [DbCommand(sql: "UPDATE MyTable SET Value = @Value", UseSnakeCase = true)]
        public partial class UpdateCommand : ICommand
        {
            public string MyValue { get; set; } // Should become my_value
        }
        """;
        var expectedDbOps = """
        // <auto-generated/>
        #nullable enable

        namespace TestNamespace;

        public partial class UpdateCommand : global::Operations.Extensions.Dapper.IDbParamsProvider
        {
            public global::Dapper.DynamicParameters ToDbParams()
