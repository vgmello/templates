// Runtime tests for DbCommandAttribute's ToDbParams generation
[DbCommand(UseSnakeCase = true)] // Test ToDbParams with snake case
public partial record SimpleCommandRuntime(string Name, int Value);

[DbCommand(UseSnakeCase = false)] // Test ToDbParams with default (pascal case)
public partial record PascalCaseCommandRuntime([property: Column("custom_name")] string SomeProperty, int AnotherValue);


public class DbCommandSourceGeneratorTests // Renamed class
{
    // --- Runtime Tests for ToDbParams generation ---
    [Fact]
    public void SimpleCommand_Runtime_GeneratesCorrectSnakeCaseParams()
    {
        var command = new SimpleCommandRuntime("TestName", 42);
        var provider = (IDbParamsProvider)command; // Cast to the interface generated
        Assert.NotNull(provider);
        var parameters = provider.ToDbParams();
        Assert.NotNull(parameters);
        Assert.IsType<DynamicParameters>(parameters);
        var paramNames = parameters.ParameterNames.ToList();
        Assert.Contains("name", paramNames); // Due to UseSnakeCase = true
        Assert.Contains("value", paramNames); // Due to UseSnakeCase = true
    }

    [Fact]
    public void PascalCaseCommand_Runtime_GeneratesCorrectParamsWithColumnOverride()
    {
        var command = new PascalCaseCommandRuntime("PropValue", 123);
        var provider = (IDbParamsProvider)command;
        Assert.NotNull(provider);
        var parameters = provider.ToDbParams();
        Assert.NotNull(parameters);
        Assert.IsType<DynamicParameters>(parameters);
        var paramNames = parameters.ParameterNames.ToList();
        Assert.Contains("custom_name", paramNames);  // Due to [Column]
        Assert.Contains("AnotherValue", paramNames); // Due to UseSnakeCase = false (default or explicit)
    }

