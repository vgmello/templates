    [Fact]
    public async Task DbCommand_SpOnly_NoICommand_GeneratesDbOpsOnly()
    {
        var source = """
        using Operations.Extensions.Dapper;
        namespace TestNamespace;

        [DbCommand(sp: "test_sp")] // nonQuery defaults to false
        public partial class MyCommand { public int Id { get; set; } }
        """;

        var expectedDbOps = GetExpectedToDbParamsSource("MyCommand", new[] { ("Id", "Id") }, isRecord:false, typeKindOverride: "partial class");

        var test = new CSharpSourceGeneratorTest<DbCommandSourceGenerator, XUnitVerifier>
        {
            TestState =
            {
                Sources = { source, ICommandInterfacesConstant }, // Added ICommandInterfaces for completeness if other tests need it implicitly
                GeneratedSources =
                {
                    (typeof(DbCommandSourceGenerator), "TestNamespace.MyCommand.DbOps.g.cs", expectedDbOps),
                },
                ReferenceAssemblies = Net6ReferenceAssemblies,
                AdditionalReferences = { typeof(DbCommandAttribute).Assembly, typeof(Dapper.DynamicParameters).Assembly, typeof(NpgsqlDataSource).Assembly }
            }
        };
        test.TestState.GeneratedSources.RemoveAt(1);
        await test.RunAsync();
        Assert.Single(test.Result.GeneratedSources);
    }

    [Fact]
    public async Task DbCommand_SqlOnly_ICommand_GeneratesDbOpsAndHandler_NonQueryImplicit()
    {
        var source = """
        using Operations.Extensions.Dapper;
        using Operations.Extensions.Messaging;
        namespace TestNamespace;

        // nonQuery defaults to false, but for ICommand (non-generic), it's treated as non-query by generator.
        [DbCommand(sql: "UPDATE MyTable SET Value = @Value", UseSnakeCase = true)]
        public partial class UpdateCommand : ICommand
        {
            public string MyValue { get; set; }
        }
        """;
        var expectedDbOps = GetExpectedToDbParamsSource("UpdateCommand", new[] { ("MyValue", "my_value") }, isRecord: false, typeKindOverride: "partial class");
        var expectedHandler = """
        // <auto-generated/>
        #nullable enable

        using System.Threading.Tasks;
        using Dapper;
        using System.Data;

        namespace TestNamespace;

        public static class UpdateCommandHandler
        {
            public static async Task HandleAsync(global::TestNamespace.UpdateCommand command, global::Npgsql.NpgsqlDataSource dataSource, global::System.Threading.CancellationToken cancellationToken = default)
            {
                await using var connection = await dataSource.OpenConnectionAsync(cancellationToken);
                var dbParams = command.ToDbParams();
                await connection.ExecuteAsync(new CommandDefinition("UPDATE MyTable SET Value = @Value", dbParams, commandType: CommandType.Text, cancellationToken: cancellationToken));
            }
        }

        """;
        await new CSharpSourceGeneratorTest<DbCommandSourceGenerator, XUnitVerifier>
        {
            TestState =
            {
                Sources = { source, ICommandInterfacesConstant },
                GeneratedSources =
                {
                    (typeof(DbCommandSourceGenerator), "TestNamespace.UpdateCommand.DbOps.g.cs", expectedDbOps),
                    (typeof(DbCommandSourceGenerator), "TestNamespace.UpdateCommand.UpdateCommandHandler.g.cs", expectedHandler),
                },
                ReferenceAssemblies = Net6ReferenceAssemblies,
                AdditionalReferences = { typeof(DbCommandAttribute).Assembly, typeof(ICommand).Assembly, typeof(Dapper.DynamicParameters).Assembly, typeof(NpgsqlDataSource).Assembly }
            }
        }.RunAsync();
    }

