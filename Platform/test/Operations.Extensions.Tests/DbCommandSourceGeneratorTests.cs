    [Fact]
    public async Task CommandWithModelResult_NonQueryTrue_GeneratesWarningAndExecuteAsync()
    {
        var someModelSource = @"namespace TestNamespace { public class SomeModel { public string? Data { get; set; } } }";
        var source = @"
        using Operations.Extensions.Dapper;
        using Operations.Extensions.Messaging;
        using System;

        namespace TestNamespace;

        [DbCommand(sp: ""sp_log_action"", nonQuery: true)]
        public partial record MyModelCommandNonQuery : ICommand<SomeModel>
        {
            public Guid Id { get; set; }
        }
        ";

        var expectedHandler = @"// <auto-generated/>
#nullable enable

using System.Threading.Tasks;
using Dapper;
using System.Data;

namespace TestNamespace;

public static class MyModelCommandNonQueryHandler
