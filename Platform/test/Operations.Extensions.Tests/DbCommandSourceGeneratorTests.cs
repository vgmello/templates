    // --- Helper Constants and Methods ---
    public const string ICommandInterfacesConstant = @"
    namespace Operations.Extensions.Messaging
    {
        public interface ICommand { }
        public interface ICommand<TResult> : ICommand { }
    }
    ";

    private string GetExpectedToDbParamsSource(string className, IEnumerable<(string PropName, string ParamName)> properties, string ns = "TestNamespace", bool isRecord = true, string? typeKindOverride = null)
    {
        var propsBuilder = new StringBuilder();
        foreach (var (propName, paramName) in properties)
        {
            propsBuilder.AppendLine($"            p.Add(\"{paramName}\", this.{propName});");
        }
        string kind = typeKindOverride ?? (isRecord ? "partial record" : "partial class");
        // Ensure there's a space if kind is not empty and doesn't end with one (e.g. "partial record class")
        if (!string.IsNullOrEmpty(kind) && !kind.EndsWith(" ")) kind += " ";


        return @$"// <auto-generated/>
#nullable enable

namespace {ns};

public {kind}{className} : global::Operations.Extensions.Dapper.IDbParamsProvider
{{
    public global::Dapper.DynamicParameters ToDbParams()
    {{
        var p = new global::Dapper.DynamicParameters();
{propsBuilder}            return p;
    }}
}}
";
    }


    // --- Runtime Tests for ToDbParams generation ---
    [Fact]
    public void SimpleCommand_Runtime_GeneratesCorrectSnakeCaseParams()
    {
        var command = new SimpleCommandRuntime("TestName", 42);
        var provider = (IDbParamsProvider)command;
        Assert.NotNull(provider);
        var parameters = provider.ToDbParams();
        Assert.NotNull(parameters);
        Assert.IsType<DynamicParameters>(parameters);
        var paramNames = parameters.ParameterNames.ToList();
        Assert.Contains("name", paramNames);
        Assert.Contains("value", paramNames);
    }

    [Fact]
    public void PascalCaseCommand_Runtime_GeneratesCorrectParamsWithColumnOverride()
    {
        var command = new PascalCaseCommandRuntime("PropValue", 123);
        var provider = (IDbParamsProvider)command;
        Assert.NotNull(provider);
        var parameters = provider.ToDbParams();
        Assert.NotNull(parameters);
        Assert.IsType<DynamicParameters>(parameters);
        var paramNames = parameters.ParameterNames.ToList();
        Assert.Contains("custom_name", paramNames);
        Assert.Contains("AnotherValue", paramNames);
    }

